package uta.cse4361.businessobjects;

/**
 *
 * @author Admin
 */
public class User {
    private int id;
    private String email;
    private String name;
    private String department;
    private int rank;

    public User(int id, String email, String name, String department, int rank) {
        this.id = id;
        this.email = email;
        this.name = name;
        this.department = department;
        this.rank = rank;
    }
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public int getRank() {
        return rank;
    }

    public void setRank(int rank) {
        this.rank = rank;
    }
    
    
}
